package ComparisonOperators;

public class Main {
    public static void main(String[] args) {
        int x = 1;
        int y = 5;

        System.out.println(x == y); // true
        System.out.println(x != y); // false
        System.out.println(x >= y); // false
        System.out.println(x <= y); // true
    }
}
